#!/usr/bin/env python3
"""This function takes a float as input and returns its string
representation, which can be useful for displaying the float
in a user-friendly format or for output purposes."""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
    n (float): The input float.

    Returns:
    str: The string representation of the input float.
    """
    return str(n)
